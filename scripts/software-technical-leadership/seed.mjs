// Writer
// -----------------------------------------------------------------------------
function loadRoadmap() {
  // We re-derive root titles/summaries from the canonical roadmap.json so we
  // never drift between roadmap and topic files.
  const raw = readFileSync(resolve(SUBJECT_DIR, 'roadmap.json'), 'utf8')
  return JSON.parse(raw)
}

function indexRoadmapNodes(roadmap) {
  const byStage = new Map()
  for (const stage of roadmap.stages) {
    const byTopic = new Map()
    for (const n of stage.nodes) {
      byTopic.set(n.topicId ?? n.id, n)
    }
    byStage.set(stage.id, byTopic)
  }
  return byStage
}

function writeTopicFile(id, meta) {
  const dir = resolve(TOPICS_DIR, id)
  mkdirSync(dir, { recursive: true })
  writeFileSync(resolve(dir, 'topic.json'), JSON.stringify(meta, null, 2) + '\n')
}

function topicExists(id) {
  return existsSync(resolve(TOPICS_DIR, id))
}

function seedPart(partNumber) {
  const part = PARTS[partNumber]
  if (!part) {
    throw new Error(`Unknown part ${partNumber}. Available: ${Object.keys(PARTS).join(', ')}`)
  }
  const roadmap = loadRoadmap()
  const byStage = indexRoadmapNodes(roadmap)

  let written = 0
  let skipped = 0

  for (const stage of part.stages) {
    const stageNodes = byStage.get(stage.id)
    if (!stageNodes) {
      throw new Error(`Stage ${stage.id} not present in roadmap.json`)
    }
    const stageTag = STAGE_TAG[stage.id]
    const rootLevel = stage.level ?? 'beginner'

    let rootOrder = 1
    for (const root of stage.roots) {
      const roadmapNode = stageNodes.get(root.id)
      if (!roadmapNode) {
        throw new Error(`Root ${root.id} not present in stage ${stage.id} roadmap`)
      }
      const level = root.level ?? rootLevel

      if (topicExists(root.id)) {
        skipped += 1
      } else {
        writeTopicFile(root.id, {
          id: root.id,
          title: roadmapNode.title,
          summary: roadmapNode.description ?? roadmapNode.title,
          order: rootOrder,
          level,
          tags: [stageTag],
        })
        written += 1
      }
      rootOrder += 1

      seedChildren(root.children ?? [], root.id, level, stageTag)
    }
  }

  console.log(`[software-technical-leadership] part ${partNumber}: wrote ${written}, skipped ${skipped}.`)
  return { written, skipped }

  function seedChildren(children, parentId, parentLevel, tag) {
    let order = 1
    for (const child of children) {
      const childId = `${parentId}--${child.id}`
      const childLevel = child.level ?? parentLevel
      if (topicExists(childId)) {
        skipped += 1
      } else {
        writeTopicFile(childId, {
          id: childId,
          title: child.title,
          summary: child.title,
          order,
          level: childLevel,
          tags: [tag],
          parentId,
        })
        written += 1
      }
      order += 1
      if (child.children?.length) {
        seedChildren(child.children, childId, childLevel, tag)
      }
    }
  }
}

// -----------------------------------------------------------------------------
// Entry point
// -----------------------------------------------------------------------------
const arg = process.argv[2] ?? '2'
if (arg === 'all') {
  for (const k of Object.keys(PARTS).map(Number).sort((a, b) => a - b)) {
    seedPart(k)
  }
} else {
  seedPart(Number(arg))
}
