import { addTopics } from './addTopics.mjs'

/** Phase template (same set of subtopics for every case study). */
const PHASES = [
  ['clarify-requirements', 'Clarify Requirements'],
  ['functional-requirements', 'Functional Requirements'],
  ['non-functional-requirements', 'Non-Functional Requirements'],
  ['capacity-estimation', 'Capacity Estimation'],
  ['api-design', 'API Design'],
  ['data-model', 'Data Model'],
  ['high-level-design', 'High-Level Design'],
  ['component-deep-dives', 'Component Deep Dives'],
  ['scaling-bottlenecks', 'Scaling & Bottlenecks'],
  ['trade-offs', 'Trade-offs'],
  ['failure-modes', 'Failure Modes & Recovery'],
  ['follow-up-questions', 'Follow-Up Questions'],
]

/**
 * Build flattened subtopic entries for a case study root.
 * Each phase becomes `{rootId}--{phaseId}` with parentId = rootId.
 * Extras are case-study-specific subtopics (own ids, parentId = rootId).
 */
function caseStudy(rootId, extras = []) {
  const items = []
  for (const [phaseId, phaseTitle] of PHASES) {
    items.push({
      id: `${rootId}--${phaseId}`,
      title: phaseTitle,
      parentId: rootId,
    })
  }
  for (const extra of extras) {
    items.push({ ...extra, parentId: rootId })
  }
  return items
}

