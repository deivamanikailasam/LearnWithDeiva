const LEVELS = new Set(['beginner', 'intermediate', 'advanced'])

function buildTopicFiles(stage, node, parentId, order, out) {
  const id = parentId ? `${parentId}--${node.slug}` : node.slug
  const meta = {
    id,
    title: node.title,
    summary: node.title,
    order,
    level: LEVELS.has(stage.level) ? stage.level : 'beginner',
    tags: [stage.id],
  }
  if (parentId) meta.parentId = parentId
  out.push(meta)
  ;(node.children ?? []).forEach((child, i) =>
    buildTopicFiles(stage, child, id, i + 1, out),
  )
}

